"""
Multi-Agent Patterns:

1. Supervisor → Workers
   - Supervisor reads task, delegates to specialist workers
   - Workers complete tasks, return to supervisor
   - Supervisor decides when to finish
   Best for: complex tasks needing different expertise

2. Pipeline (Sequential)
   - Research → Draft → Review → Publish
   - Each agent does one phase, hands off to next
   Best for: content creation, data processing workflows

3. Parallel
   - Multiple agents run simultaneously
   - Results combined by a final node
   Best for: analysis from multiple perspectives, batch processing

Key insight:
  "Multi-agent" = multiple calls to LLMs with different system prompts + tools
  Each agent is just: specialized system prompt + relevant tools

Next: Human-in-the-Loop — pause the graph and wait for human approval
"""
